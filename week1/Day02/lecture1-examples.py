# operators >, >=, <, <=, ==, !=
x = int(input("what's x? "))
y = int(input("what's y? "))

# only if statements
'''
if x < y:
    print("x is less than y")   
if x > y:
    print("x is greater than y")
if x == y:
    print("x is equal to y")
'''

# with elif statement
'''
if x < y:
    print("x is less than y")   
elif x > y:
    print("x is greater than y")
elif x == y:
    print("x is equal to y")
'''

# with if-else statement
'''
if x < y:
    print("x is less than y")   
elif x > y:
    print("x is greater than y")
else x == y:
    print("x is equal to y")
'''

# or statement
'''
if x < y or x > y:
    print("x is not equal to y")
else:
    print("x is equal to y")
'''


